import functools

numbers = [6, 10, 2]

def comparator(a,b):
    t1 = a + b
    t2 = b + a
    return (int(t1) > int(t2)) - (int(t1) < int(t2)) 

def solution(numbers) :
    n = [str(x) for x in numbers]
    n = sorted(n, key=functools.cmp_to_key(comparator), reverse=True)
    
    answer = str(int(''.join(n)))

    return answer

print(solution(numbers))
